public class Calculadora {

    // int soma(int, int)
    public static int soma(int n1, int n2) {
        return n1 + n2;
    }

    // double soma(double, double)
    public static double soma(double n1, double n2) {
        return n1 + n2;
    }

    public static int divisao(int n1, int n2) {
        return n1 / n2;
    }
}
