public class Questao06 {
  public static void main(String[] args) {
    int i = 10;
    while (i > 0) {
      System.out.println(i);
      i--;
    }
    System.out.println("Fogo!!!");

    int j = 10;
    do {
      System.out.println(j);
      j--;
    } while (j > 0);
    System.out.println("Fogo!!!");

    for (int k = 10; k > 0; k--) {
      System.out.println(k);
    }
    System.out.println("Fogo!!!");
  }
}
